-- =============================================================================
-- AEI MODULE — DDL
-- Prefix: AEI
-- Asociații de Economii și Împrumut (Credit Unions)
-- =============================================================================

-- ---------------------------------------------------------------------------
-- 1. Membri (Members)
-- ---------------------------------------------------------------------------
CREATE TABLE AEI_MEMBERS (
    MEMBER_ID       NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    IDNP            VARCHAR2(13),            -- personal code (citizens)
    IDNO            VARCHAR2(13),            -- fiscal code (legal entities)
    MEMBER_TYPE     VARCHAR2(10) DEFAULT 'PERSON' NOT NULL,  -- PERSON / COMPANY
    LAST_NAME       VARCHAR2(100) NOT NULL,
    FIRST_NAME      VARCHAR2(100),
    FULL_NAME       VARCHAR2(250),           -- computed or entered
    PHONE           VARCHAR2(30),
    EMAIL           VARCHAR2(120),
    ADDRESS         VARCHAR2(300),
    BIRTH_DATE      DATE,
    JOIN_DATE       DATE DEFAULT SYSDATE,
    STATUS          VARCHAR2(20) DEFAULT 'ACTIVE',  -- ACTIVE / INACTIVE / BLOCKED
    NOTES           VARCHAR2(1000),
    CREATED_AT      TIMESTAMP DEFAULT SYSTIMESTAMP,
    UPDATED_AT      TIMESTAMP DEFAULT SYSTIMESTAMP
);

CREATE INDEX AEI_MEMBERS_IDNP_IDX  ON AEI_MEMBERS(IDNP);
CREATE INDEX AEI_MEMBERS_NAME_IDX  ON AEI_MEMBERS(LAST_NAME, FIRST_NAME);
CREATE INDEX AEI_MEMBERS_STATUS_IDX ON AEI_MEMBERS(STATUS);

-- ---------------------------------------------------------------------------
-- 2. Depozite — contracte (Deposit contracts)
-- ---------------------------------------------------------------------------
CREATE TABLE AEI_DEPOSITS (
    DEPOSIT_ID      NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    CONTRACT_NO     VARCHAR2(50) NOT NULL UNIQUE,
    MEMBER_ID       NUMBER NOT NULL REFERENCES AEI_MEMBERS(MEMBER_ID),
    SIGN_DATE       DATE NOT NULL,           -- data semnării contractului
    START_DATE      DATE NOT NULL,           -- data încasării sumei inițiale
    END_DATE        DATE NOT NULL,           -- data expirării contractului
    PRINCIPAL       NUMBER(18,2) NOT NULL,   -- suma inițială depusă
    CURRENCY        VARCHAR2(3) DEFAULT 'MDL',
    INTEREST_RATE   NUMBER(8,4) NOT NULL,    -- rata dobânzii anuale (%)
    PENALTY_RATE    NUMBER(8,4) DEFAULT 1,   -- rata dobânzii datorii sp. plată (%)
    TAX_RATE        NUMBER(8,4) DEFAULT 6,   -- impozit pe dobândă (%)
    CAPITALIZATION  VARCHAR2(20) DEFAULT 'QUARTERLY',  -- MONTHLY/QUARTERLY/ANNUAL/NONE
    DAY_COUNT       VARCHAR2(10) DEFAULT 'ACT365',     -- ACT365 / ACT360 / 30360
    STATUS          VARCHAR2(20) DEFAULT 'ACTIVE',     -- ACTIVE / EXPIRED / TERMINATED / RENEWED
    PARENT_ID       NUMBER REFERENCES AEI_DEPOSITS(DEPOSIT_ID),  -- при пролонгации
    NOTES           VARCHAR2(1000),
    CREATED_AT      TIMESTAMP DEFAULT SYSTIMESTAMP,
    UPDATED_AT      TIMESTAMP DEFAULT SYSTIMESTAMP
);

CREATE INDEX AEI_DEPOSITS_MEMBER_IDX  ON AEI_DEPOSITS(MEMBER_ID);
CREATE INDEX AEI_DEPOSITS_STATUS_IDX  ON AEI_DEPOSITS(STATUS);
CREATE INDEX AEI_DEPOSITS_DATES_IDX   ON AEI_DEPOSITS(START_DATE, END_DATE);

-- ---------------------------------------------------------------------------
-- 3. Depozite — fluxuri zilnice (Deposit transactions)
-- ---------------------------------------------------------------------------
CREATE TABLE AEI_DEPOSIT_FLOWS (
    FLOW_ID         NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    DEPOSIT_ID      NUMBER NOT NULL REFERENCES AEI_DEPOSITS(DEPOSIT_ID),
    FLOW_DATE       DATE NOT NULL,
    FLOW_TYPE       VARCHAR2(30) NOT NULL,
    -- TYPES: PRINCIPAL_IN, PRINCIPAL_OUT, PRINCIPAL_TO_DUE,
    --        INTEREST_CALC, INTEREST_TAX, INTEREST_TO_DUE,
    --        INTEREST_CAPITALIZE, INTEREST_PAY, TAX_PAY
    DAYS            NUMBER(5) DEFAULT 1,
    AMOUNT          NUMBER(18,4) NOT NULL,
    BALANCE_5131    NUMBER(18,4) DEFAULT 0,  -- sold 5131 Depuneri TS
    BALANCE_51321   NUMBER(18,4) DEFAULT 0,  -- sold 51321 Dobânzi calc.
    BALANCE_51322   NUMBER(18,4) DEFAULT 0,  -- sold 51322 Dobânzi dat sp.plată
    BALANCE_5133    NUMBER(18,4) DEFAULT 0,  -- sold 5133 Datorii sp.plată
    BALANCE_51331   NUMBER(18,4) DEFAULT 0,  -- sold 51331 Principal sp.plată
    BALANCE_51332   NUMBER(18,4) DEFAULT 0,  -- sold 51332 Dobânzi sp.plată
    BALANCE_5343    NUMBER(18,4) DEFAULT 0,  -- sold 5343 Imp.pe venit
    DESCRIPTION     VARCHAR2(500),
    CREATED_AT      TIMESTAMP DEFAULT SYSTIMESTAMP
);

CREATE INDEX AEI_DEP_FLOWS_DEP_IDX  ON AEI_DEPOSIT_FLOWS(DEPOSIT_ID);
CREATE INDEX AEI_DEP_FLOWS_DATE_IDX ON AEI_DEPOSIT_FLOWS(FLOW_DATE);
CREATE INDEX AEI_DEP_FLOWS_TYPE_IDX ON AEI_DEPOSIT_FLOWS(FLOW_TYPE);

-- ---------------------------------------------------------------------------
-- 4. Credite — contracte (Loan contracts)
-- ---------------------------------------------------------------------------
CREATE TABLE AEI_LOANS (
    LOAN_ID         NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    CONTRACT_NO     VARCHAR2(50) NOT NULL UNIQUE,
    MEMBER_ID       NUMBER NOT NULL REFERENCES AEI_MEMBERS(MEMBER_ID),
    SIGN_DATE       DATE NOT NULL,
    DISBURSEMENT_DATE DATE NOT NULL,
    END_DATE        DATE NOT NULL,
    PRINCIPAL       NUMBER(18,2) NOT NULL,
    CURRENCY        VARCHAR2(3) DEFAULT 'MDL',
    INTEREST_RATE   NUMBER(8,4) NOT NULL,   -- rata anuală (%)
    PENALTY_RATE    NUMBER(8,4) DEFAULT 0.1, -- penalități restanță (%/zi)
    REPAYMENT_TYPE  VARCHAR2(20) DEFAULT 'ANNUITY',  -- ANNUITY / DECLINING
    TERM_MONTHS     NUMBER(5) NOT NULL,
    PURPOSE         VARCHAR2(300),
    COLLATERAL      VARCHAR2(500),
    STATUS          VARCHAR2(20) DEFAULT 'ACTIVE',  -- ACTIVE / CLOSED / OVERDUE / RESTRUCTURED
    RISK_CLASS      VARCHAR2(5) DEFAULT 'S',         -- S/W/D/D1/P (CNPF classes)
    PROVISION_PCT   NUMBER(8,4) DEFAULT 0,
    NOTES           VARCHAR2(1000),
    CREATED_AT      TIMESTAMP DEFAULT SYSTIMESTAMP,
    UPDATED_AT      TIMESTAMP DEFAULT SYSTIMESTAMP
);

CREATE INDEX AEI_LOANS_MEMBER_IDX ON AEI_LOANS(MEMBER_ID);
CREATE INDEX AEI_LOANS_STATUS_IDX ON AEI_LOANS(STATUS);

-- ---------------------------------------------------------------------------
-- 5. Credite — grafic + plăți (Loan schedule & payments)
-- ---------------------------------------------------------------------------
CREATE TABLE AEI_LOAN_FLOWS (
    FLOW_ID         NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    LOAN_ID         NUMBER NOT NULL REFERENCES AEI_LOANS(LOAN_ID),
    DUE_DATE        DATE NOT NULL,
    FLOW_TYPE       VARCHAR2(30) NOT NULL,
    -- TYPES: SCHEDULE_PRINCIPAL, SCHEDULE_INTEREST,
    --        PAYMENT_PRINCIPAL, PAYMENT_INTEREST, PENALTY_CALC, PENALTY_PAY
    AMOUNT_SCHEDULED NUMBER(18,4) DEFAULT 0,
    AMOUNT_PAID      NUMBER(18,4) DEFAULT 0,
    AMOUNT_OVERDUE   NUMBER(18,4) DEFAULT 0,
    BALANCE_PRINCIPAL NUMBER(18,4) DEFAULT 0,
    PAYMENT_DATE    DATE,
    DAYS_OVERDUE    NUMBER(5) DEFAULT 0,
    DESCRIPTION     VARCHAR2(500),
    CREATED_AT      TIMESTAMP DEFAULT SYSTIMESTAMP
);

CREATE INDEX AEI_LOAN_FLOWS_LOAN_IDX ON AEI_LOAN_FLOWS(LOAN_ID);
CREATE INDEX AEI_LOAN_FLOWS_DATE_IDX ON AEI_LOAN_FLOWS(DUE_DATE);

