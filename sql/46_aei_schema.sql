-- ---------------------------------------------------------------------------
-- 9. Event log
-- ---------------------------------------------------------------------------
CREATE TABLE AEI_EVENT_LOG (
    LOG_ID          NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    LOG_TS          TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    EVENT_TYPE      VARCHAR2(50) NOT NULL,
    SOURCE_TABLE    VARCHAR2(50),
    SOURCE_ID       NUMBER,
    USER_NAME       VARCHAR2(100),
    DESCRIPTION     VARCHAR2(2000),
    OLD_VALUES      CLOB,
    NEW_VALUES      CLOB
);

CREATE INDEX AEI_EVENT_LOG_TS_IDX   ON AEI_EVENT_LOG(LOG_TS);
CREATE INDEX AEI_EVENT_LOG_TYPE_IDX ON AEI_EVENT_LOG(EVENT_TYPE);
