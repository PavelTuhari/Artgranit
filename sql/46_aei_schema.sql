-- ---------------------------------------------------------------------------
-- 7. Jurnal contabil (Accounting journal)
-- ---------------------------------------------------------------------------
CREATE TABLE AEI_JOURNAL (
    ENTRY_ID        NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    ENTRY_DATE      DATE NOT NULL,
    ENTRY_NO        VARCHAR2(30),
    DEBIT_ACCOUNT   VARCHAR2(20) NOT NULL,
    CREDIT_ACCOUNT  VARCHAR2(20) NOT NULL,
    EXTRA_DEBIT     VARCHAR2(20),            -- cont extrabilanțier majorare
    EXTRA_CREDIT    VARCHAR2(20),            -- cont extrabilanțier diminuare
    AMOUNT          NUMBER(18,4) NOT NULL,
    DESCRIPTION     VARCHAR2(500),
    DOCUMENT_REF    VARCHAR2(200),           -- document justificativ
    SOURCE_TYPE     VARCHAR2(30),            -- DEPOSIT / LOAN / MANUAL
    SOURCE_ID       NUMBER,                  -- DEPOSIT_ID or LOAN_ID
    FLOW_ID         NUMBER,                  -- AEI_DEPOSIT_FLOWS.FLOW_ID
    CREATED_BY      VARCHAR2(100),
    CREATED_AT      TIMESTAMP DEFAULT SYSTIMESTAMP
);

CREATE INDEX AEI_JOURNAL_DATE_IDX   ON AEI_JOURNAL(ENTRY_DATE);
CREATE INDEX AEI_JOURNAL_DEBIT_IDX  ON AEI_JOURNAL(DEBIT_ACCOUNT);
CREATE INDEX AEI_JOURNAL_CREDIT_IDX ON AEI_JOURNAL(CREDIT_ACCOUNT);
CREATE INDEX AEI_JOURNAL_SOURCE_IDX ON AEI_JOURNAL(SOURCE_TYPE, SOURCE_ID);

