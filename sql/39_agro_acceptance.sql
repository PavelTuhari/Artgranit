-- 5.1 Audit trigger for field requests status changes
CREATE OR REPLACE TRIGGER AGRO_FIELD_REQUESTS_AUDIT
  AFTER UPDATE ON AGRO_FIELD_REQUESTS FOR EACH ROW
DECLARE
  v_user VARCHAR2(100) := SYS_CONTEXT('USERENV','CLIENT_IDENTIFIER');
  PROCEDURE log_change(p_field VARCHAR2, p_old VARCHAR2, p_new VARCHAR2) IS
  BEGIN
    IF NVL(p_old,'~') != NVL(p_new,'~') THEN
      INSERT INTO AGRO_AUDIT_LOG (TABLE_NAME, RECORD_ID, ACTION, FIELD_NAME, OLD_VALUE, NEW_VALUE, CHANGED_BY)
      VALUES ('AGRO_FIELD_REQUESTS', :OLD.ID, 'update', p_field, p_old, p_new, v_user);
    END IF;
  END log_change;
BEGIN
  log_change('STATUS', :OLD.STATUS, :NEW.STATUS);
END;
/

