-- ============================================================
-- Colass Contracts module (CLS_CONTRACT_*)
-- Conceptual clone of classic contract registry from GARA contracte module
-- Integrated with Colass CRM leads and Estimator
-- ============================================================

CREATE TABLE CLS_CONTRACTS (
  ID               NUMBER          NOT NULL,
  CONTRACT_NO      VARCHAR2(80)    NOT NULL,
  CONTRACT_DATE    DATE            DEFAULT TRUNC(SYSDATE) NOT NULL,
  LEGAL_FORM       VARCHAR2(60),
  COMPANY_NAME     VARCHAR2(500),
  ADDRESS          VARCHAR2(1000),
  FISCAL_CODE      VARCHAR2(120),
  VAT_CODE         VARCHAR2(120),
  BANK_NAME        VARCHAR2(500),
  BANK_BRANCH      VARCHAR2(500),
  BANK_BIC         VARCHAR2(120),
  IBAN_MDL         VARCHAR2(120),
  IBAN_EUR         VARCHAR2(120),
  IBAN_USD         VARCHAR2(120),
  CURRENCY         VARCHAR2(10)    DEFAULT 'MDL' NOT NULL,
  TOTAL_AMOUNT     NUMBER(18,4)    DEFAULT 0 NOT NULL,
  STATUS           VARCHAR2(40)    DEFAULT 'DRAFT' NOT NULL,
  NOTES            CLOB,
  PROJECT_ID       NUMBER,
  ESTIMATE_ID      NUMBER,
  CRM_LEAD_ID      NUMBER,
  CRM_CONTRACT_ID  NUMBER,
  CREATED_BY       VARCHAR2(120),
  CREATED_AT       TIMESTAMP       DEFAULT CURRENT_TIMESTAMP NOT NULL,
  UPDATED_AT       TIMESTAMP       DEFAULT CURRENT_TIMESTAMP NOT NULL,
  CONSTRAINT PK_CLS_CONTRACTS PRIMARY KEY (ID),
  CONSTRAINT UK_CLS_CONTRACTS_NO UNIQUE (CONTRACT_NO),
  CONSTRAINT FK_CLS_CONTRACTS_PROJECT FOREIGN KEY (PROJECT_ID) REFERENCES CLS_PROJECTS(ID),
  CONSTRAINT FK_CLS_CONTRACTS_ESTIMATE FOREIGN KEY (ESTIMATE_ID) REFERENCES CLS_ESTIMATES(ID),
  CONSTRAINT FK_CLS_CONTRACTS_LEAD FOREIGN KEY (CRM_LEAD_ID) REFERENCES CLS_CRM_LEADS(ID),
  CONSTRAINT FK_CLS_CONTRACTS_CRM_CONTRACT FOREIGN KEY (CRM_CONTRACT_ID) REFERENCES CLS_CRM_CONTRACTS(ID)
);
/

