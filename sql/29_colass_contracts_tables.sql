CREATE OR REPLACE VIEW V_CLS_CONTRACTS AS
SELECT
  c.ID,
  c.CONTRACT_NO,
  c.CONTRACT_DATE,
  c.LEGAL_FORM,
  c.COMPANY_NAME,
  c.ADDRESS,
  c.FISCAL_CODE,
  c.VAT_CODE,
  c.BANK_NAME,
  c.BANK_BRANCH,
  c.BANK_BIC,
  c.IBAN_MDL,
  c.IBAN_EUR,
  c.IBAN_USD,
  c.CURRENCY,
  c.TOTAL_AMOUNT,
  c.STATUS,
  c.NOTES,
  c.PROJECT_ID,
  p.NAME AS PROJECT_NAME,
  c.ESTIMATE_ID,
  e.NAME AS ESTIMATE_NAME,
  c.CRM_LEAD_ID,
  l.LEAD_NO,
  l.CONTACT_NAME AS LEAD_CONTACT_NAME,
  l.COMPANY_NAME AS LEAD_COMPANY_NAME,
  c.CRM_CONTRACT_ID,
  cc.CONTRACT_NO AS CRM_CONTRACT_NO,
  (SELECT COUNT(*) FROM CLS_CONTRACT_ITEMS ci WHERE ci.CONTRACT_ID = c.ID) AS ITEMS_COUNT,
  (SELECT NVL(SUM(ci.AMOUNT),0) FROM CLS_CONTRACT_ITEMS ci WHERE ci.CONTRACT_ID = c.ID) AS ITEMS_TOTAL_AMOUNT,
  c.CREATED_BY,
  c.CREATED_AT,
  c.UPDATED_AT
FROM CLS_CONTRACTS c
LEFT JOIN CLS_PROJECTS p ON p.ID = c.PROJECT_ID
LEFT JOIN CLS_ESTIMATES e ON e.ID = c.ESTIMATE_ID
LEFT JOIN CLS_CRM_LEADS l ON l.ID = c.CRM_LEAD_ID
LEFT JOIN CLS_CRM_CONTRACTS cc ON cc.ID = c.CRM_CONTRACT_ID;
/

CREATE OR REPLACE VIEW V_CLS_CONTRACT_ITEMS_TOTALS AS
SELECT
  ci.CONTRACT_ID,
  NVL(rt.CODE, 'OTHER') AS TYPE_CODE,
  NVL(rt.NAME_RU, 'Прочее') AS TYPE_NAME_RU,
  NVL(rt.NAME_RO, 'Altele') AS TYPE_NAME_RO,
  COUNT(ci.ID) AS ITEMS_COUNT,
  NVL(SUM(ci.AMOUNT), 0) AS TOTAL_AMOUNT
FROM CLS_CONTRACT_ITEMS ci
LEFT JOIN CLS_RESOURCE_TYPES rt ON rt.ID = ci.RESOURCE_TYPE_ID
GROUP BY ci.CONTRACT_ID, NVL(rt.CODE, 'OTHER'), NVL(rt.NAME_RU, 'Прочее'), NVL(rt.NAME_RO, 'Altele');
/

COMMIT;
/
