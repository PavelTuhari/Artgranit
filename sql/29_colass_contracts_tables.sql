  QTY               NUMBER(18,6)    DEFAULT 0 NOT NULL,
  PRICE             NUMBER(18,4)    DEFAULT 0 NOT NULL,
  AMOUNT            NUMBER(18,4)    DEFAULT 0 NOT NULL,
