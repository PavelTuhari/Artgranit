  CONSTRAINT FK_CLS_CONTRACT_ITEMS_CONTRACT FOREIGN KEY (CONTRACT_ID) REFERENCES CLS_CONTRACTS(ID),
  CONSTRAINT FK_CLS_CONTRACT_ITEMS_EST_ITEM FOREIGN KEY (ESTIMATE_ITEM_ID) REFERENCES CLS_ESTIMATE_ITEMS(ID),
  CONSTRAINT FK_CLS_CONTRACT_ITEMS_TYPE FOREIGN KEY (RESOURCE_TYPE_ID) REFERENCES CLS_RESOURCE_TYPES(ID)
);
/

