from __future__ import annotations

import json
import threading
from typing import Any

import oracledb

from .database import DatabaseConnection


_SCHEMA_LOCK = threading.RLock()
_SCHEMA_READY = False
_CRED_LOG_TABLE = "CRED_EVENT_LOG"
_CRED_LOG_TS_INDEX = "IX_CRED_EVENT_LOG_TS"


def _db_error_code(exc: Exception) -> int | None:
    err = getattr(exc, "args", [None])[0]
    return getattr(err, "code", None)


def ensure_runtime_schema() -> None:
    global _SCHEMA_READY
    if _SCHEMA_READY:
        return

    with _SCHEMA_LOCK:
        if _SCHEMA_READY:
            return

        connection = DatabaseConnection.get_connection()
        try:
            with connection.cursor() as cursor:
                for statement in (
                    """
                    CREATE TABLE CRED_EVENT_LOG (
                        ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                        TS_ISO VARCHAR2(40) NOT NULL,
                        LEVEL_NAME VARCHAR2(16) NOT NULL,
                        SOURCE_NAME VARCHAR2(200) NOT NULL,
                        MESSAGE_TEXT CLOB,
                        PAYLOAD_JSON CLOB
                    )
                    """,
                    "CREATE INDEX IX_CRED_EVENT_LOG_TS ON CRED_EVENT_LOG (TS_ISO)",
                ):
                    try:
                        cursor.execute(statement)
                    except oracledb.DatabaseError as exc:
                        if _db_error_code(exc) != 955:
                            raise
            connection.commit()
            _SCHEMA_READY = True
        finally:
            connection.close()


def _read_lob(value: Any) -> str:
    if value is None:
        return ""
    if hasattr(value, "read"):
        value = value.read()
    if isinstance(value, bytes):
        return value.decode("utf-8")
    return str(value)


